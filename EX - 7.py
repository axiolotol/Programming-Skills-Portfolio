# Loop that counts up from 0 to 50 in increments of 1
print("Counting up from 0 to 50 in increments of 1:")
for i in range(0, 51):  # The range starts at 0 and goes up to 50 (inclusive)
    print(i)

# Loop that counts down from 50 to 0 in decrements of 1
print("\nCounting down from 50 to 0 in decrements of 1:")
for i in range(50, -1, -1):  # Start at 50, go down to 0 (inclusive), step -1
    print(i)

# Loop that counts up from 30 to 50 in increments of 1
print("\nCounting up from 30 to 50 in increments of 1:")
for i in range(30, 51):  # Start at 30, go up to 50 (inclusive)
    print(i)

# Loop that counts down from 50 to 10 in decrements of 2
print("\nCounting down from 50 to 10 in decrements of 2:")
for i in range(50, 9, -2):  # Start at 50, go down to 10 (inclusive), step -2
    print(i)

# Loop that counts up from 100 to 200 in increments of 5
print("\nCounting up from 100 to 200 in increments of 5:")
for i in range(100, 201, 5):  # Start at 100, go up to 200 (inclusive), step 5
    print(i)
